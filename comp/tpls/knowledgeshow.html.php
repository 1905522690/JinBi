        抗战金银纪念币涨3倍 
        <br /><br />
        近日，记者走访了崇仁路邮币卡市场，在钱币的摊位采访几位摊主，他们不约而同地推荐了抗战纪念银币。"一盎司的抗战纪念银币卖得是最好的。发行价格是300多，目前市场上卖1000出头。价格虽然涨了3倍，但需求量依然很大。"一个收藏店的老板告诉记者。 
        <br /><br />
        抗战纪念金银币早在发行之前就被收藏市场看好。首先，抗战70周年的宣传力度非常大。抗战纪念金银币发行之前，央视新闻联播和各主要权威媒体都对这一事 件进行了报道，尤其是国务院新闻办公室还举办了专场新闻发布会，向全国的主流媒体详细解读了发行该套金银币的情况。这极大提升了公众对这套金银币的知晓度 和关注度，增强了大众购买这套金银币的兴趣和意愿。 
        <br /><br />
        其次，抗战金银币题材非常好。从金银币市场来看，题材好的金银币，即使价格不暴涨，也是一直稳中有升的。普通大众觉得纪念意义大于投资意义，1000多块的价格虽然涨得高，但还是在接受范围内，喜欢这个题材的人会买回去收藏和赏玩。 
        <br /><br />
        最后，抗战胜利七十周年的普通币发行量很大，达到了五亿。普通群众在银行都可以购买到。购买了普币以后，他们其中一部分可能会对精致的金银币发生兴趣，这又把一部分也许以前不收藏金银币的人吸引进了金银币市场。 
        <br /><br />
        不可能跌破的面值 
        <br /><br />
        最近两年，金银币市场一直是不温不火。此前，金银币市场有两大支撑点。一个就是收藏和投资的人，二就是企业定制礼品。企业定制礼品的数量在这两年锐减，这让不少的钱币老板都叫苦不迭。本来就是硬通货，就算市场行情再不好，它作为 贵重金属的价值是永远不会改变的。从收藏角度来看，精制币以制作精美，把玩程度高而更受欢迎。当然，精制币因为发行数量少，价格会更贵。
